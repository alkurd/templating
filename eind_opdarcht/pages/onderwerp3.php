  <h2>1. Wat is MySQL?</h2>
  <p>MySQL is een populair, open-source relationeel databasesysteem dat wordt gebruikt voor het opslaan en beheren van grote hoeveelheden gegevens. Het is snel, betrouwbaar en geschikt voor zowel kleine als grote toepassingen.</p>

  <h2>2. Tabellen en datatypes</h2>
  <p>In MySQL worden gegevens opgeslagen in tabellen, die bestaan uit rijen en kolommen. Elke kolom heeft een datatype, zoals <code>INT</code> voor gehele getallen, <code>VARCHAR</code> voor tekst, of <code>DATE</code> voor datums.</p>

  <h2>3. SQL-queries schrijven</h2>
  <p>Met SQL (Structured Query Language) kun je gegevens opvragen (<code>SELECT</code>), toevoegen (<code>INSERT</code>), wijzigen (<code>UPDATE</code>) en verwijderen (<code>DELETE</code>) uit je database.</p>

  <h2>4. Integratie met PHP</h2>
  <p>MySQL wordt vaak gebruikt in combinatie met PHP om dynamische websites te maken. PHP kan via extensies zoals <code>mysqli</code> of <code>PDO</code> communiceren met een MySQL-database voor het ophalen en opslaan van gegevens.</p>
