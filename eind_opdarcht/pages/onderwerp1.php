  <h2>1. Syntax en basisprincipes</h2>
  <p>PHP lijkt qua structuur op C en JavaScript, wat het toegankelijk maakt voor veel ontwikkelaars. Een typisch PHP-script begint met <code>&lt;?php</code> en eindigt met <code>?&gt;</code>. Variabelen beginnen met <code>$</code> en de taal ondersteunt standaard controlestructuren zoals <code>if</code>, <code>while</code>, en <code>for</code>.</p>

  <h2>2. Interactie met HTML en formulieren</h2>
  <p>Een van de krachtigste eigenschappen van PHP is hoe het gemakkelijk te integreren is met HTML. PHP kan formulieren verwerken, zoals contactformulieren of inlogschermen, door gegevens via <code>$_POST</code> of <code>$_GET</code> op te halen.</p>

  <h2>3. Werken met databases</h2>
  <p>PHP wordt vaak gecombineerd met MySQL om dynamische, datagedreven websites te maken. Met <code>mysqli</code> of <code>PDO</code> kun je gegevens opvragen, toevoegen, bewerken of verwijderen in een database.</p>

  <h2>4. Beveiliging en best practices</h2>
  <p>Hoewel PHP krachtig is, vereist het ook zorgvuldige beveiliging. Ontwikkelaars moeten input valideren, SQL-injecties voorkomen en veilige sessiebeheer toepassen om kwetsbaarheden te vermijden.</p>

  <p>PHP blijft een fundamentele bouwsteen van het moderne web.</p>